
public class swap_with_variable {
	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		int t;
		System.out.println(a);
		System.out.println(b);
		
		t=a;
		a=b;
		b=t;
		
		System.out.println("-------------------------------------------");
		System.out.println(a);
        System.out.println(b);		
		
		
	}

}
